
public class Card {
	private double credit;
	private int tickets;
	private int num;
	
	void setCredit(double newCredit)
	{
		this.credit = newCredit;
	}
	double getCredit()
	{
		return this.credit;
	}
	void setTickets(int newTickets)
	{
		this.tickets = newTickets;
	}
	int getTickets()
	{
		return this.tickets;
	}
	void setNum(int newNum)
	{
		this.num = newNum;
	}
	int getNum()
	{
		return this.num;
	}
}

